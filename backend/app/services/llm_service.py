async def get_chat_response(message: str, language: str = "en") -> str:
    if not bedrock_client:
        return "I'm sorry, my AI processing engine is currently offline. Please try again later."
    
    system_prompt = CHAT_SYSTEM_PROMPT
    if language == "hi":
        system_prompt += "\nकृपया हिंदी में उत्तर दें।"

    try:
        response = await asyncio.to_thread(_invoke_bedrock_converse, system_prompt, message)
        return response
    except Exception as e:
        logger.error(f"Chat generation failed: {e}")
        return "I encountered an error while processing your request. Please try again."

async def generate_weather_report(weather: Dict, language: str = "en") -> str:
    """Generate a comprehensive weather report/advisory for a farmer."""
    if not bedrock_client:
        return "Weather analysis is currently offline. Please check the raw data below."

    system_prompt = """You are KisaanDrishti AI Weather Expert. 
Your task is to analyze weather data and provide a detailed SITUATIONAL REPORT for a farmer.
Explain what the weather means for:
1. Irrigation (Should they water or will rain be enough?)
2. Fertilizer application (Is it safe to apply or will it wash away?)
3. Pest risk (Does high humidity or heat suggest pest outbreaks?)
4. Harvesting (Is the weather clear enough for drying crops?)
Use RICH MARKDOWN with headers, bold text, and bullet points. Be professional and encouraging.
"""
    if language == "hi":
        system_prompt += "\nकृपया हिंदी में विस्तार से उत्तर दें।"

    # Build a concise summary for the LLM
    daily = weather.get("daily", [])
    today_h = weather.get("today_hourly", [])
    
    summary_lines = ["WEATHER DATA OVERVIEW:"]
    for d in daily[:3]: # Next 3 days
        summary_lines.append(f"- Date: {d['date']} | Max: {d['temp_max']}C | Min: {d['temp_min']}C | Rain: {d['precipitation_mm']}mm | Prob: {d['precip_prob_pct']}%")
    
    if today_h:
        summary_lines.append("\nTODAY'S HOURLY PEAKS (Humidity & Wind):")
        # Extract max wind and avg humidity for brevity
        humidities = [h['humidity'] for h in today_h if 'humidity' in h]
        winds = [h['wind_speed'] for h in today_h if 'wind_speed' in h]
        avg_h = sum(humidities)/len(humidities) if humidities else 0
        max_w = max(winds) if winds else 0
        summary_lines.append(f"- Avg Humidity: {avg_h:.1f}% | Max Wind: {max_w:.1f} km/h")

    user_message = "\n".join(summary_lines)

    try:
        explanation = await asyncio.to_thread(_invoke_bedrock_converse, system_prompt, user_message)
        return explanation
    except Exception as e:
        logger.error(f"Weather report generation failed: {e}")
        return "Failed to generate AI weather report. Please check raw forecast charts."


def _fallback_explanation(soil: SoilInput, result: PrescriptionResult, language: str) -> str:
    lines = [f"Analysis for {soil.crop_name}:"]
    for p in result.products_chemical:
        lines.append(f"- {p.product_name}: {p.kg_per_ha} kg/ha ({p.application_timing})")
    return "\n".join(lines)
