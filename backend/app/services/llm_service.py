settings = get_settings()

bedrock_client = None
if settings.AWS_ACCESS_KEY_ID:
    try:
        bedrock_client = boto3.client(
            service_name="bedrock-runtime",
            region_name=settings.AWS_REGION,
            aws_access_key_id=settings.AWS_ACCESS_KEY_ID,
            aws_secret_access_key=settings.AWS_SECRET_ACCESS_KEY,
        )
    except Exception as e:
        logger.error(f"Failed to initialize Boto3 client: {e}")

SYSTEM_PROMPT_EN = """You are KisaanDrishti AI, an expert agronomist assistant for farmers in India.

Your job is to EXPLAIN a fertilizer prescription generated by a scientific STCR engine.
STRICT RULES:
1. You MUST NOT invent any numeric doses. Use ONLY the given numbers.
2. Use RICH MARKDOWN formatting (bold text, lists, headers) to make the text highly readable and well-structured.
3. Be comprehensive and detailed. Explain WHY each fertilizer is needed and the scientific benefit.
4. I will provide you with TODAY'S fully detailed hourly weather data. Analyze it deeply and provide an EXACT recommended time window *today* to go out and apply the fertilizer (look for periods < 35°C, no rain). If today is completely unsafe, state exactly why based on the hourly data.
"""

SYSTEM_PROMPT_HI = """आप KisaanDrishti AI हैं, भारत के किसानों के लिए एक कृषि विशेषज्ञ।

आपका काम है वैज्ञानिक STCR इंजन द्वारा उत्पन्न उर्वरक prescription को समझाना।
सख्त नियम:
1. आप कोई भी संख्या (मात्रा) स्वयं नहीं बनाएंगे।
2. पाठ को अत्यधिक पठनीय और अच्छी तरह से संरचित बनाने के लिए समृद्ध मार्कडाउन (MARKDOWN) स्वरूपण (बोल्ड टेक्स्ट, सूचियां, हेडर) का उपयोग करें।
3. व्यापक और विस्तृत रहें। समझाएं कि प्रत्येक उर्वरक क्यों आवश्यक है और विज्ञान क्या है।
4. मैं आपको आज के मौसम का घंटे-दर-घंटे डेटा प्रदान करूंगा। इसका विश्लेषण करें और आज छिड़काव के लिए सबसे अच्छा समय सुझाएं (जब तापमान 35°C से कम हो और बारिश न हो)।
"""

def _build_prescription_summary(soil: SoilInput, result: PrescriptionResult, weather: Optional[Dict], farm_size: float = 1.0, farm_unit: str = "Hectare") -> str:
    lines = [
        f"Crop: {soil.crop_name} | Target Yield: {soil.target_yield_q_ha} q/ha",
        f"Farm Size: {farm_size} {farm_unit}",
        f"Soil: N={soil.n_kg_ha}, P={soil.p_kg_ha}, K={soil.k_kg_ha}",
        "PRESCRIPTION (Rates scaled to Farm Size):",
    ]
    
    # Calculate scale factor
    scale = 1.0
    if farm_unit.lower() == "acre":
        scale = farm_size * 0.404686
    elif farm_unit.lower() == "bigha":
        scale = farm_size * 0.25
    else:
        scale = farm_size

    for p in result.products_chemical:
        total_kg = p.kg_per_ha * scale
        lines.append(f"  - {p.product_name}: {p.kg_per_ha} kg/ha (TOTAL REQUIRED: {total_kg:.1f} kg) [{p.application_timing}]")
    
    if result.warnings:
        lines.append("\nWARNINGS:")
        for w in result.warnings:
            lines.append(f"  - {w}")
            
    if weather and "today_hourly" in weather:
        lines.append("\n--- TODAY'S HOURLY WEATHER DATA ---")
        for hr in weather["today_hourly"]:
            lines.append(f"Time: {hr['time']} | Temp: {hr['temp']}C | Precip: {hr['precip_mm']}mm")
        lines.append("----------------------------------\n")
            
    return "\n".join(lines)


def _invoke_bedrock_converse(system_prompt: str, user_message: str) -> str:
    if not bedrock_client:
        raise ValueError("Bedrock client not initialized")
    try:
        response = bedrock_client.converse(
            modelId=settings.BEDROCK_NOVA_PRO_ID,
            messages=[{"role": "user", "content": [{"text": user_message}]}],
            system=[{"text": system_prompt}],
            inferenceConfig={"temperature": 0.3, "maxTokens": 500}
        )
        return response["output"]["message"]["content"][0]["text"]
    except ClientError as e:
        logger.error(f"Bedrock API error: {e}")
        raise e

async def generate_explanation(soil: SoilInput, result: PrescriptionResult, language: str = "en", weather: Optional[Dict] = None, farm_size: float = 1.0, farm_unit: str = "Hectare") -> str:
    if not bedrock_client:
        return _fallback_explanation(soil, result, language)

    summary = _build_prescription_summary(soil, result, weather, farm_size, farm_unit)
    system_prompt = SYSTEM_PROMPT_HI if language == "hi" else SYSTEM_PROMPT_EN
    user_message = f"Please explain this prescription to the farmer. IMPORTANT: You must explicitly mention the total kilograms required for their specific {farm_size} {farm_unit} farm size as calculated in the payload below.\n\n{summary}"

    try:
        # Run synchronous boto3 call in thread pool to not block asyncio event loop
        explanation = await asyncio.to_thread(_invoke_bedrock_converse, system_prompt, user_message)
        return explanation
    except Exception as e:
        logger.error(f"LLM Generation failed: {e}")
        return _fallback_explanation(soil, result, language)


